// Estimating efficiency

// Input size  ---------- Required time complexity
// n <= 10                O(n!)
// n <= 20                O(2^n)
// n <= 500               O(n^3)
// n <= 5000              O(n^2)
// n <= 2e5               O(n*sqrt(n))
// n <= 1e6               O(n*log2(n))
// n <= 1e8               O(n)
// n <= 1e16              O(sqrt(n))
// n is large             O(1) or O(logn)